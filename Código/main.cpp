void getSensorValues() {
  float count = 100;
  for (int i = 0; i <= count; i++) {
    mpu.getEvent(&a, &g, &temp);
    dataGyroX += g.gyro.x;
    dataGyroY += g.gyro.y;
    dataGyroZ += g.gyro.z;
  }
  gyroscopeX = dataGyroX / count;
  gyroscopeY = dataGyroY / count;
  gyroscopeZ = dataGyroZ / count;

  dataGyroX = 0;
  dataGyroY = 0;
  dataGyroZ = 0;

  magGyro= sqrt(gyroscopeX*gyroscopeX+gyroscopeY*gyroscopeY+gyroscopeZ*gyroscopeZ);
}

