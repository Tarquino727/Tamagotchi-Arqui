void morir(void *pvParameters) {
  (void)pvParameters;

  // Nmero de veces que se mostrarn los frames del corazn antes de mostrar la tumba
  const int heartCycleCount = 1;
  static unsigned long lastStateChange = 0;
  static int heartCycle = 0;  // Contador para los ciclos de corazn
  static int currentState = 0; // Estado inicial
  
  for (;;) {
    // Adquiere el mutex antes de operaciones crticas en la pantalla
    if (xSemaphoreTake(semaforoMorir, portMAX_DELAY) == pdTRUE) {

      unsigned long currentTime = millis();

      // Solo cambia de estado si no estamos en el ltimo frame
      if (currentState < 8) {
        // Cambiar de estado despus de stateDuration
        if (currentTime - lastStateChange >= stateDuration) {
          currentState = (currentState + 1) % 9; // Cambiar al siguiente estado
          lastStateChange = currentTime;
        }
      }

      // Interpolacin entre los frames consecutivos
      const uint8_t *currentFrame;
      switch (currentState) {
        case 0: currentFrame = epd_bitmap_corazon1;  break;
        case 1: currentFrame = epd_bitmap_corazon2; break;
        case 2: currentFrame = epd_bitmap_corazon3;  break;
        case 3: currentFrame = epd_bitmap_corazon4;  break;
        case 4: currentFrame = epd_bitmap_corazon5;  break;
        case 5: currentFrame = epd_bitmap_corazon6;  break;
        case 6: currentFrame = epd_bitmap_corazon7;  break;
        case 7: currentFrame = epd_bitmap_flash;  break;
        case 8: currentFrame = epd_bitmap_tumba; break;
      }

      // Mostrar el frame interpolado en la pantalla
      display.clearDisplay();
      display.drawBitmap(0, 0, currentFrame, 128, 64, 1);
      display.display();
	  Serial.print("Ya te carg el payaso");

      // Incrementar el contador de ciclos de corazn si hemos mostrado el frame de la tumba
      if (currentState == 7) {
        heartCycle++;
      }

      // Cambiar al estado inicial si hemos mostrado suficientes ciclos de corazn
      if (heartCycle >= heartCycleCount) {
        currentState = 8;  // Cambiar al estado inicial
      }

      xSemaphoreGive(semaforoMorir);
      vTaskDelay(pdMS_TO_TICKS(150));
    }
    vTaskDelay(pdMS_TO_TICKS(150));
  }
}
